"""
String Slicing
-> Normal slicing using START & END-1 range of the string index
-> Negative slicing using START & END-1 range of the string index
"""

names = "Pramod, Acharya"
# Normal slicing
print(names[0:6])

# Without specifying the START range of the string
print(names[:6])

# Negative slicing
print(names[-7:-1])
